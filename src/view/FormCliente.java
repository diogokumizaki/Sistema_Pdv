    // Validaçao para salvar os dados dos campos
    public boolean validacaoCampos() {
        if (!txtNome.getText().equals("") && !txtSobrenome.getText().equals("") && !txtCpf.getText().equals("   .   .   -  ") && !txtRg.getText().equals("       ") && !txtDataNasc.getText().equals("  /  /    ") && !txtBairro.getText().equals("") && !txtEndereco.getText().equals("") && !txtNumero.getText().equals("") && !txtCelular.getText().equals("(  )     -    ")) {
            return true;
        } else {
            return false;
        }
    }

