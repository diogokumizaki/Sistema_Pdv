    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // Envia os dados para serem salvos
        if (validacaoCampos()) {
            //ModeloTabelaCliente cliente = new ModeloTabelaCliente();
            String data = txtDataNasc.getText();
            String[] aux = data.split("/");
            String dia = aux[0];
            String mes = aux[1];
            String ano = aux[2];
            String novadata = ano + "-" + mes + "-" + dia;
            modelo.insert(new Cliente(txtNome.getText(), txtSobrenome.getText(), novadata, txtRg.getText(), txtCpf.getText(), txtBairro.getText(), txtEndereco.getText(), txtNumero.getText(), txtCelular.getText()));
            modelo.atualizarTabela();
            limparCampos();
            painel.setSelectedComponent(abaListagem);
        } else {
            JOptionPane.showMessageDialog(null, "Ainda existe campo(s) em branco! Por favor preencha.", "Atenção", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // Envia os dados para serem atualizados
        if (!txtCodigo.getText().equals("") && validacaoCampos()) {
            int resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja salvar as alteraçoes?", "Alteraçao", JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {
                String data = txtDataNasc.getText();
                String[] aux = data.split("/");
                String dia = aux[0];
                String mes = aux[1];
                String ano = aux[2];
                String novadata = ano + "-" + mes + "-" + dia;
                modelo.update(new Cliente(Integer.parseInt(txtCodigo.getText()), txtNome.getText(), txtSobrenome.getText(), novadata, txtRg.getText(), txtCpf.getText(), txtBairro.getText(), txtEndereco.getText(), txtNumero.getText(), txtCelular.getText()));
                modelo.atualizarTabela();
                limparCampos();
                painel.setSelectedComponent(abaListagem);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Os dados nao foram alterados", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(rootPane, "Verifique os campos se estao todos preenchidos corretamente!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        // Envia o codigo do cliente para ser deletado
        if (txtCodigo.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Nenhum cliente foi selecionado! Selecione primeiro!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
        } else {
            int resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja excluir o cliente " + txtNome.getText() + "? Todos os dados relacionados a suas compras tambem serao excluidos!", "Remoçao", JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {
                modelo.delete(Integer.parseInt(txtCodigo.getText()));
                modelo.atualizarTabela();
                limparCampos();
                painel.setSelectedComponent(abaListagem);
            } else {
                JOptionPane.showMessageDialog(rootPane, "O cliente nao foi excluido!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            }

        }

    }//GEN-LAST:event_btnDeletarActionPerformed

